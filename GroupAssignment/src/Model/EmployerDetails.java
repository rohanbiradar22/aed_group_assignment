public class EmployerDetails {
    
    private String username;
    private String password;
    private String StudentCredits;
    private String StudentTranscripts;
    private String ProfessorRatings;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getStudentCredits() {
        return StudentCredits;
    }

    public void setStudentCredits(String StudentCredits) {
        this.StudentCredits = StudentCredits;
    }

    public String getStudentTranscripts() {
        return StudentTranscripts;
    }

    public void setStudentTranscripts(String StudentTranscripts) {
        this.StudentTranscripts = StudentTranscripts;
    }

    public String getProfessorRatings() {
        return ProfessorRatings;
    }

    public void setProfessorRatings(String ProfessorRatings) {
        this.ProfessorRatings = ProfessorRatings;
    }

    
    
    
}